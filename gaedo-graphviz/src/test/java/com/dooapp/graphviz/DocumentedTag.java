package com.dooapp.graphviz;

public class DocumentedTag {
	private Long id;
	private String text;
	private String comment;
	private double interest;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the text
	 */
	public String getText() {
		return text;
	}

	public DocumentedTag(String text) {
		super();
		this.text = text;
	}

	/**
	 * @param text
	 *            the text to set
	 */
	public void setText(String text) {
		this.text = text;
	}
}
